//datatypes in java
public class javabasics3{
    public static void main(String args[]){
        byte b =8;
        System.out.println(b);
        char ch = 'j';
        System.out.println(ch);
        boolean value = true;
        System.out.println(value);
        float price = 10/5;
        System.out.println(price);
        int num = 20;
        System.out.println(num);
        short n = 200;
        System.out.println(n);
    }
}